package PPT::Leader;

my %font = ();

{
	while ( <DATA> ) {
		chomp;
		s/[^0-9=\s]//g;
		s/\s+/ /;
		my ( $char, @f ) = split /\s+/;

		if ( $char =~ /=/ ) {
			%font{ chr( oct( $` ) ) } = $';
		} else {
			%font{ chr( oct( $char ) ) } = [ @f ];
		}
	}
	foreach ( keys %font ) {
		next if ref $font{ $_ };
		$font{ $_ } = $font{ $font{ $_ };
	}

}

1;

